import type { Species, SpeciesVariant } from '../../types'

function createDragonbornVariant(params: {
  id: string
  name: string
  nameIT: string
  family: 'chromatic' | 'gem' | 'metallic'
  familyIT: string
  damageType: string
  damageTypeIT: string
  breathShape: 'cone' | 'line'
  breathAreaFeet: number
  breathAreaIT: string
  familyFeatureName: string
  familyFeatureNameIT: string
  familyFeatureDescription: string
  familyFeatureDescriptionIT: string
}): SpeciesVariant {
  const {
    id,
    name,
    nameIT,
    family,
    familyIT,
    damageType,
    damageTypeIT,
    breathShape,
    breathAreaFeet,
    breathAreaIT,
    familyFeatureName,
    familyFeatureNameIT,
    familyFeatureDescription,
    familyFeatureDescriptionIT,
  } = params

  return {
    id,
    name,
    nameIT,
    description: `${familyIT} dragonborn tied to a ${name.toLowerCase()} dragon. Their breath weapon deals ${damageType} damage, and they resist ${damageType} damage.`,
    descriptionIT: `Dragonide ${familyIT.toLowerCase()} legato a un drago ${nameIT.toLowerCase()}. Il suo soffio infligge danni ${damageTypeIT.toLowerCase()} e concede resistenza allo stesso tipo di danno.`,
    mechanics: {
      family,
      familyIT,
      damageType,
      damageTypeIT,
      resistanceType: damageType,
      resistanceTypeIT: damageTypeIT,
      breathShape,
      breathAreaFeet,
      breathAreaIT,
    },
    features: [
      {
        name: `${name} Breath Weapon`,
        nameIT: `Soffio ${nameIT}`,
        description: `Your Breath Weapon deals ${damageType} damage in a ${breathAreaFeet}-foot ${breathShape}. The saving throw is based on your Constitution, and the damage scales as you gain levels.`,
        descriptionIT: `Il tuo Soffio infligge danni ${damageTypeIT.toLowerCase()} in un${breathShape === 'cone' ? ' cono' : 'a linea'} di ${breathAreaIT}. Il tiro salvezza usa Costituzione e i danni scalano con il livello.`,
        level: 1,
      },
      {
        name: `${name} Resistance`,
        nameIT: `Resistenza ${nameIT}`,
        description: `You have resistance to ${damageType} damage.`,
        descriptionIT: `Hai resistenza ai danni ${damageTypeIT.toLowerCase()}.`,
        level: 1,
      },
      {
        name: familyFeatureName,
        nameIT: familyFeatureNameIT,
        description: familyFeatureDescription,
        descriptionIT: familyFeatureDescriptionIT,
        level: 5,
      },
    ],
  }
}

